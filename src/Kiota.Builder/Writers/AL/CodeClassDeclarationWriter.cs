using System;
using System.Linq;
using Kiota.Builder.CodeDOM;
using Kiota.Builder.Extensions;

namespace Kiota.Builder.Writers.AL;

public class CodeClassDeclarationWriter : BaseElementWriter<ClassDeclaration, ALConventionService>
{
    public static string AutoGenerationHeader => "// <auto-generated/>";

    public CodeClassDeclarationWriter(ALConventionService conventionService) : base(conventionService) { }

    public override void WriteCodeElement(ClassDeclaration codeElement, LanguageWriter writer)
    {
        ArgumentNullException.ThrowIfNull(codeElement);
        ArgumentNullException.ThrowIfNull(writer);

        if (codeElement.Parent is not CodeClass parentClass)
            return;
        if (parentClass.ParentIsSkipped()) // seems to be a nested class, we don't support that in AL
            return;
        if (parentClass.Parent is not CodeNamespace) // seems to be a nested class, we don't support that in AL
        {
            parentClass.CustomData["skip"] = "true";
            return;
        }

        // Check if this is a parameter codeunit
        if (parentClass.CustomData.TryGetValue("parameter-codeunit", out var paramCu) &&
            paramCu.Equals("true", StringComparison.OrdinalIgnoreCase))
        {
            WriteParameterCodeunit(parentClass, codeElement, writer);
            return;
        }

        // Auto-generation header
        writer.WriteLine(AutoGenerationHeader);

        // Namespace
        if (parentClass.Parent is CodeNamespace parentNs && !string.IsNullOrEmpty(parentNs.Name))
        {
            writer.WriteLine($"namespace {parentNs.Name};");
            writer.WriteLine();
        }

        // Usings
        WriteUsings(codeElement, writer);

        // Documentation
        WriteDocumentation(parentClass, writer);

        // Object ID
        parentClass.CustomData.TryGetValue("object-id", out var objectId);
        objectId ??= "0";

        // Class name (no namespace prefix - AL does not allow dots in object names)
        var className = parentClass.Name;

        // Implements
        var implements = codeElement.Implements.ToList();
        var implementsClause = implements.Count > 0
            ? $" implements {string.Join(", ", implements.Select(i => $"\"{i.Name}\""))}"
            : string.Empty;

        // Pragma disable for object declaration
        parentClass.CustomData.TryGetValue("pragmas", out var pragmas);
        if (!string.IsNullOrEmpty(pragmas))
            writer.WriteLine($"#pragma warning disable {pragmas}");

        writer.WriteLine($"codeunit {objectId} \"{className}\"{implementsClause}");

        if (!string.IsNullOrEmpty(pragmas))
            writer.WriteLine($"#pragma warning restore {pragmas}");

        writer.StartBlock();

        // Object properties
        WriteObjectProperties(parentClass, writer);

        // Global variables
        WriteGlobalVariables(parentClass, writer);
    }

    private static void WriteUsings(ClassDeclaration codeElement, LanguageWriter writer)
    {
        var usings = codeElement.Usings
            .Select(u => u.IsExternal ? u.Declaration?.Name ?? u.Name : u.Name)
            .Where(u => !string.IsNullOrEmpty(u))
            .Distinct(StringComparer.OrdinalIgnoreCase)
            .Order(StringComparer.OrdinalIgnoreCase)
            .ToList();

        if (usings.Count == 0) return;

        foreach (var u in usings)
            writer.WriteLine($"using {u};");
        writer.WriteLine();
    }

    private static void WriteDocumentation(CodeClass parentClass, LanguageWriter writer)
    {
        if (parentClass.Documentation?.DescriptionAvailable == true)
        {
            var description = parentClass.Documentation.GetDescription(static t => t.Name);
            // Strip any CustomData metadata from description
            if (!string.IsNullOrEmpty(description))
            {
                writer.WriteLine("/// <summary>");
                writer.WriteLine($"/// {description}");
                writer.WriteLine("/// </summary>");
            }
        }
    }

    private static void WriteObjectProperties(CodeClass parentClass, LanguageWriter writer)
    {
        var objectProps = parentClass.Properties
            .Where(p => p.CustomData.ContainsKey("object-property"))
            .ToList();

        if (objectProps.Count == 0) return;

        foreach (var prop in objectProps)
        {
            var propName = prop.Name;
            var propValue = prop.CustomData.TryGetValue("value", out var val) ? val : prop.DefaultValue;
            writer.WriteLine($"{propName} = {propValue};");
        }
        writer.WriteLine();
    }

    private void WriteGlobalVariables(CodeClass parentClass, LanguageWriter writer)
    {
        var globals = parentClass.Properties
            .Where(p => p.CustomData.ContainsKey("global-variable"))
            .ToList();

        if (globals.Count == 0) return;

        // Convert to ALVariable instances
        var variables = globals.Select(p =>
        {
            p.CustomData.TryGetValue("pragmas", out var pragmas);
            p.CustomData.TryGetValue("value", out var value);
            var isLabel = p.Type.Name.Equals("Label", StringComparison.OrdinalIgnoreCase);
            var varValue = isLabel ? (value ?? string.Empty) : string.Empty;
            var isLockedLabel = p.CustomData.TryGetValue("locked-label", out var locked) &&
                                locked.Equals("true", StringComparison.OrdinalIgnoreCase);

            return new ALVariable(p.Name, p.Type, varValue, isLockedLabel ? "Locked = true" : string.Empty, pragmas ?? string.Empty);
        }).ToList();

        // Order: Codeunit types first, then others, alphabetically within groups
        var codeunitVars = variables
            .Where(v => v.Type?.Name?.StartsWith("Codeunit", StringComparison.OrdinalIgnoreCase) == true)
            .OrderBy(v => v.Type?.Name ?? string.Empty, StringComparer.OrdinalIgnoreCase)
            .ToList();
        var otherVars = variables
            .Where(v => v.Type?.Name?.StartsWith("Codeunit", StringComparison.OrdinalIgnoreCase) != true)
            .OrderBy(v => v.Type?.Name ?? string.Empty, StringComparer.OrdinalIgnoreCase)
            .ToList();

        var ordered = codeunitVars.Concat(otherVars).ToList();

        writer.WriteLine("var");
        writer.IncreaseIndent();

        // Pragma disable for variables
        parentClass.CustomData.TryGetValue("pragmas-variables", out var varPragmas);
        if (!string.IsNullOrEmpty(varPragmas))
            writer.WriteLine($"#pragma warning disable {varPragmas}");

        foreach (var v in ordered)
            v.Write(writer, conventions);

        if (!string.IsNullOrEmpty(varPragmas))
            writer.WriteLine($"#pragma warning restore {varPragmas}");

        writer.DecreaseIndent();
        writer.WriteLine();
    }

    private void WriteParameterCodeunit(CodeClass parentClass, ClassDeclaration codeElement, LanguageWriter writer)
    {
        // Auto-generation header
        writer.WriteLine(AutoGenerationHeader);

        // Namespace
        if (parentClass.Parent is CodeNamespace parentNs && !string.IsNullOrEmpty(parentNs.Name))
        {
            writer.WriteLine($"namespace {parentNs.Name};");
            writer.WriteLine();
        }

        // Usings
        WriteUsings(codeElement, writer);

        // Object ID
        parentClass.CustomData.TryGetValue("object-id", out var objectId);
        objectId ??= "0";

        writer.WriteLine($"codeunit {objectId} \"{parentClass.Name}\"");
        writer.StartBlock();

        // Dictionary variable
        writer.WriteLine("var");
        writer.IncreaseIndent();
        writer.WriteLine("QueryParameters: Dictionary of [Text, Text];");
        writer.DecreaseIndent();
        writer.WriteLine();

        // SetQueryParameter method
        writer.WriteLine("procedure SetQueryParameter(QueryKey: Text; QueryValue: Text)");
        writer.WriteLine("begin");
        writer.IncreaseIndent();
        writer.WriteLine("if QueryParameters.ContainsKey(QueryKey) then");
        writer.IncreaseIndent();
        writer.WriteLine("QueryParameters.Remove(QueryKey);");
        writer.DecreaseIndent();
        writer.WriteLine("QueryParameters.Add(QueryKey, QueryValue);");
        writer.DecreaseIndent();
        writer.WriteLine("end;");
        writer.WriteLine();

        // Parse query parameters from CustomData
        if (parentClass.CustomData.TryGetValue("query-parameters", out var queryParamsStr) &&
            !string.IsNullOrEmpty(queryParamsStr))
        {
            var queryParams = queryParamsStr.Split(',', StringSplitOptions.RemoveEmptyEntries);
            foreach (var qp in queryParams)
            {
                var parts = qp.Split(':', 2);
                var paramName = parts[0].Trim();
                var paramType = parts.Length > 1 ? parts[1].Trim() : "Text";
                var alType = conventions.GetTypeString(new CodeType { Name = paramType }, parentClass);
                var isText = alType.Equals("Text", StringComparison.OrdinalIgnoreCase);
                var valueExpr = isText ? "Value" : "Format(Value)";

                writer.WriteLine($"procedure Set{paramName.ToFirstCharacterUpperCase()}(Value: {alType})");
                writer.WriteLine("begin");
                writer.IncreaseIndent();
                writer.WriteLine($"this.SetQueryParameter('{paramName.ToFirstCharacterLowerCase()}', {valueExpr});");
                writer.DecreaseIndent();
                writer.WriteLine("end;");
                writer.WriteLine();
            }
        }

        // GetQueryParameters method
        writer.WriteLine("procedure GetQueryParameters(): Dictionary of [Text, Text]");
        writer.WriteLine("begin");
        writer.IncreaseIndent();
        writer.WriteLine("exit(QueryParameters);");
        writer.DecreaseIndent();
        writer.WriteLine("end;");

        writer.CloseBlock();
    }
}
